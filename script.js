function updateServicesAndFaqs() {
  const services = getServices()
  const serviceCards = document.querySelectorAll(".service-card")
  serviceCards.forEach((card, index) => {
    if (services[index]) {
      const service = services[index]
      const titleElement = card.querySelector("h3")
      const descriptionElement = card.querySelector("p")
      const priceElement = card.querySelector(".price")
      const durationElement = card.querySelector(".service-duration")

      if (titleElement) titleElement.textContent = service.title
      if (descriptionElement) descriptionElement.textContent = service.description
      if (priceElement) priceElement.textContent = service.price
      if (durationElement) durationElement.textContent = service.duration
    }
  })

  const faqs = getFaqs()
  const faqItems = document.querySelectorAll(".faq-item")
  faqItems.forEach((item, index) => {
    if (faqs[index]) {
      const faq = faqs[index]
      const questionElement = item.querySelector(".faq-question span:first-child")
      const answerElement = item.querySelector(".faq-answer p")

      if (questionElement) questionElement.textContent = faq.question
      if (answerElement) answerElement.textContent = faq.answer
    }
  })
}
